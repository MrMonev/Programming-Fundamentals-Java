public class DemoChars {
    public static void main(String[] args) {
        char letter = 'T';
        int number = 66;


        System.out.println((int) letter);
        // аски код на моя символ
        System.out.println((char) number);
        // символ с дадения аски код
        System.out.println('a' + 'c');
        // int + int = int (събираме техните стойниски на аски)
        System.out.println("" + 'a' + 'c');
        // долепваме двата символа
    }
}
